    private void tabelaNomes(){
        modeloTabela = (DefaultTableModel)jTable1.getModel();       
        ArrayList listaNomes = rnNome.buscaNomes(jTextField1.getText());
        int linha = -1;
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        modeloTabela.setNumRows(listaNomes.size());
        Iterator listaNomesIt = listaNomes.iterator();
        while(listaNomesIt.hasNext()){
            linha ++;
            nb = (NomeBeans)listaNomesIt.next();
            modeloTabela.setValueAt(nb.getId(), linha, 0);
            modeloTabela.setValueAt(nb.getNome(), linha, 1);
        }
    }

