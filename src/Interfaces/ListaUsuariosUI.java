        nb.setNome((String)modeloTabela.getValueAt(jTable1.getSelectedRow(), 1));
        CadastrarNomeUI cadastrarNomeUI = new CadastrarNomeUI(null, rootPaneCheckingEnabled);
        cadastrarNomeUI.capturaDadosUsuario(nb);
        this.dispose();
        cadastrarNomeUI.show();
        
